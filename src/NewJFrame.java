    private void jPanel2MouseEntered(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel2MouseEntered
        if (started) {
            if (time == null) {
                time = System.currentTimeMillis();
            }
            if (System.currentTimeMillis() - time > 5000) {
                jLabel1.setText("Terminado");
                //jLabel3.setText(count+"");
                started = false;
                count--;
            } else {
                jLabel3.setText(++count + "");
                int x;
                int y;
                int mousex = jPanel1.getMousePosition().x;
                int mousey = jPanel1.getMousePosition().y;

                //System.out.println(mousex + " " + mousey);
                //System.out.println((jPanel1.getWidth() - jPanel2.getWidth()) + " " + (jPanel1.getHeight() - jPanel2.getHeight()));
                do {
                    x = r.nextInt(jPanel1.getWidth() - jPanel2.getWidth());
                    //System.out.println("Set x");
                } while (mousex < x + jPanel2.getWidth() && mousex > x);

                do {
                    y = r.nextInt(jPanel1.getHeight() - jPanel2.getHeight());
                    //System.out.println("Set y");
                } while (mousey < y + jPanel2.getHeight() && mousey > y);

                //System.out.println("Location: " + x + " " + y);
                jPanel2.setLocation(x, y);
                jPanel2.setBackground(new Color(
                        (int) (Math.random() * 255),
                        (int) (Math.random() * 255),
                        (int) (Math.random() * 255)));
            }
        }
    }//GEN-LAST:event_jPanel2MouseEntered

